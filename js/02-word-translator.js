let code = prompt('Choose and enter one of these codes: es, de, fr,or en ');
if (code === 'es') {
    document.write("Hola Mundo")
} else if (code === 'de'){
    document.write("Hallo Welt")
} else if (code === 'fr'){
    document.write("Bonjour le monde")
} else {
    document.write("Hello World")
}